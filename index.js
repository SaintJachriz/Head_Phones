function Show(){
    let hide = document.getElementById("hide_1");
    let show = document.getElementById("show");
    show.style.display = "none";
     hide.style.display = "flex";
 }
 
 function Hide(){
   let hide = document.getElementById("hide_1");
   let show = document.getElementById("show");
    show.style.display = "flex";
     hide.style.display = "none";
 
 }